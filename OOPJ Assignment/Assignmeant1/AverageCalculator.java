//4. Program to calculate the average of three int numbers with typecasting

public class AverageCalculator {
    public static void main(String[] args) {
        int a = 10, b = 20, c = 30;
        double avg = (a + b + c) / 3.0; // Typecasting to double
        System.out.println("Average: " + avg);
    }
}