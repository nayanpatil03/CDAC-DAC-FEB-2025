//Q8,9,10,11 Operators 

class Operators1{
      public static void main(String[] args){
	  
	  int a = 10;
	  int b = 50;
	  
	  //Arithmeatic 
	  System.out.println(a+b);
	  System.out.println(a-b);
	  System.out.println(a*b);
	  System.out.println(a/b);
	  System.out.println(a%b);
	  
	  
	 //Relational 
	 System.out.println(a==b);
	 System.out.println(a!=b);
	 System.out.println(a>b);
	 System.out.println(a<b);
	 System.out.println(a>=b);
	 System.out.println(a<=b);
	 
	 // Logical
	 boolean x = true, y = false;
	 System.out.println(x && y);
	 System.out.println(x || y);
	 System.out.println(!y);
	 System.out.println(!x);
	 
	 // Assignment 
	 
	 System.out.println(a=b);
	 System.out.println(a+=b);
	 System.out.println(a-=b);
	 System.out.println(a*=b);
	 System.out.println(a/=b);
	 System.out.println(a%=b);
	 
	 
	 
	 
	 
	 
	  }
}