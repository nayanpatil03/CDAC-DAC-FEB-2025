// 5. Program to demonstrate different literals in Java
public class LiteralsDemo {
    public static void main(String[] args) {
        int binary = 0b1010; // Binary literal
        int octal = 012; // Octal literal
        int hex = 0xA; // Hexadecimal literal
        double scientific = 1.23e2; // Scientific notation
        
        System.out.println("Binary: " + binary);
        System.out.println("Octal: " + octal);
        System.out.println("Hexadecimal: " + hex);
        System.out.println("Scientific Notation: " + scientific);
    }
}